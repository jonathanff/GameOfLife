#import "UIColor+GOLColors.h"

@implementation UIColor (GOLColors)

+ (UIColor *)GOLPurple;
{
    return [UIColor colorWithRed:80/255.0f green:29/255.0f blue:80/255.0f alpha:1.0f];
}

+ (UIColor *)GOLOrange;
{
    return [UIColor colorWithRed:235/255.0f green:63/255.0f blue:0/255.0f alpha:1.0f];
}

@end
